# Arithmetic Operator & Assignment Operator
num1 = 19
num2 = 5

sum = num1 + num2   # Addition
sub = num1 - num2   # Subtraction
mul = num1 * num2   # Multiplication
div = num1 / num2   # Division
indiv = num1 // num2    # Integer/Floor Division
mod = num1 % num2       # Modulo
exp = num1 ** num2      # Exponent

print("Addition: ", sum)
print("Subraction: ", sub)
print("Multiplication: ", mul)
print("Division: ", div)
print("Floor Division: ", indiv)
print("Modulo: ", mod)
print("Exponent: ", exp)
