# Type Conversion

# str -> int, float, bool
x = "123"
y = int(x)

print(x, type(x))
print(y, type(y))

print("---------------------")
# int -> float, str, bool
x = 0
y =bool(x)

print(x, type(x))
print(y, type(y))

print("---------------------")
# float -> int, str, bool
x = 24.56
y = bool(x)

print(x, type(x))
print(y, type(y))

print("---------------------")
# bool -> int, float, str
x = False
y = int(x)

print(x, type(x))
print(y, type(y))


